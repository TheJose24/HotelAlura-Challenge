    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Connection connection = null;
        PreparedStatement preparedStatement = null;
        ResultSet resultSet = null;

        try {
            // Obtener el usuario y contraseña ingresados por el usuario
            String usuario = txtUsuario.getText();
            char[] contrasenaChars = txtContrasena.getPassword();
            String contrasena = new String(contrasenaChars);

            // Validar que ambos campos no estén vacíos
            if (usuario.isEmpty() || contrasena.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
                return;
            }

            // Preparar la consulta SQL con parámetros para evitar SQL injection
            String query = "SELECT * FROM login WHERE usuario = ? AND contrasena = ?";
            connection = conexion.conectar();
            preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, usuario);
            preparedStatement.setString(2, contrasena);

            // Ejecutar la consulta SQL
            resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                JOptionPane.showMessageDialog(rootPane, "Bienvenido");
                this.setVisible(false);
                MenuUsuario menu = new MenuUsuario();
                menu.setVisible(true);

            } else {
                // Mostrar mensaje de credenciales incorrectas si no se encontró el usuario
                JOptionPane.showMessageDialog(this, "Credenciales incorrectas");
            }
        } catch (SQLException ex) {
            // Manejar errores de base de datos y mostrar un mensaje de error
            JOptionPane.showMessageDialog(this, "Error de base de datos: " + ex.getMessage());
        } finally {
            // Cerrar la conexión, el PreparedStatement y el ResultSet en un bloque finally
            try {
                if (resultSet != null) {
                    resultSet.close();
                }
                if (preparedStatement != null) {
                    preparedStatement.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error al cerrar la conexion: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

